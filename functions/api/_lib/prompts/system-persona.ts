// === Mode dispatch ===
// 사용자 명시 2026-05-22: chatMode 별로 다른 ADDENDUM 결합.
// body._chatMode 가 'daily' | 'inquiry' | 'vent' → 해당 ADDENDUM (비어있으면 DEFAULT fallback)
// body._chatMode 가 null / undefined / 알 수 없는 값 → ADDENDUM_DEFAULT (자동 감지, legacy 동작)
export type ChatMode = 'daily' | 'inquiry' | 'vent' | null | undefined | string;

export function pickAddendum(chatMode: ChatMode): string {
  const normalized = typeof chatMode === 'string' ? chatMode.toLowerCase() : null;
  switch (normalized) {
    case 'daily':   return ADDENDUM_DAILY   || ADDENDUM_DEFAULT;
    case 'inquiry': return ADDENDUM_INQUIRY || ADDENDUM_DEFAULT;
    case 'vent':    return ADDENDUM_VENT    || ADDENDUM_DEFAULT;
    default:        return ADDENDUM_DEFAULT;
  }
}

export function resolvePersona(chatMode: ChatMode): string {
  return PERSONA_BASE_HEAD + '\n\n' + pickAddendum(chatMode) + '\n\n' + PERSONA_BASE_TAIL;
}

// 하위호환: SYSTEM_PERSONA = DEFAULT 모드 결합. 분할 전과 byte-identical 한 동작 보장.
export const SYSTEM_PERSONA = PERSONA_BASE_HEAD + '\n\n' + ADDENDUM_DEFAULT + '\n\n' + PERSONA_BASE_TAIL;

// SYSTEM_PERSONA 를 prepend 적용할 endpoint set.
// chat_main / analyze_4stage = 메인 대화 + 4단 깊은 분석 (19-chat/09-generate-ai-response.js).
// intake = 첫 관찰 4단 분석 (04-annual-review-phase1/06-first-observation.js _intakeAnalyze).
export const PERSONA_ENDPOINTS = new Set(['chat_main', 'analyze_4stage', 'intake']);

// body.system 형태 분기 처리 — array (cache_control 블록) / string / undefined.
// 첫 블록의 text 앞에 prepend → 첫 블록 cache_control (ephemeral) 그대로 보존 → cache prefix hit 유지.
// SYSTEM_PERSONA 가 매 호출 동일하므로 Anthropic prompt cache 가 prefix-match 로 90% 할인 적용.
export function applyPersonaToBody(body: any): void {
  if (!PERSONA_ENDPOINTS.has(body?._endpoint)) return;
  const persona = resolvePersona(body?._chatMode);
  if (Array.isArray(body.system)) {
    if (body.system.length > 0 && body.system[0]?.type === 'text') {
      const _first = body.system[0];
      body.system[0] = {
        ..._first,
        text: persona + '\n' + (_first.text || ''),
        cache_control: _first.cache_control || { type: 'ephemeral', ttl: '1h' }
      };
    } else {
      body.system.unshift({
        type: 'text',
        text: persona,
        cache_control: { type: 'ephemeral', ttl: '1h' }
      });
    }
  } else if (typeof body.system === 'string' && body.system.length > 0) {
    body.system = [{
      type: 'text',
      text: persona + '\n' + body.system,
      cache_control: { type: 'ephemeral', ttl: '1h' }
    }];
  } else {
    body.system = [{
      type: 'text',
      text: persona,
      cache_control: { type: 'ephemeral', ttl: '1h' }
    }];
  }
}
